"""THE CORE (meeting 2026-07-02): optimize semantic embeddings under the causal constraints derived from a
GIVEN graph. The optimization variables are the EMBEDDINGS themselves — latent u_j and unlabeled-observed
s_i. No encoder is trained (labeled embeddings a_i come from a frozen encoder and are fixed), so there is no
semantic-drift failure mode.

Objective (see README): visible labels must be generated by the graph; unlabeled nodes equal their
generation; latent children stay consistent with their latent parents; marginally independent pairs
decorrelate; unit-norm regularization keeps embeddings decode-stable.
"""
import numpy as np


def resolve_device(torch, device="cpu"):
    """Resolve the requested PyTorch device.

    Defaults to CPU because the current graph-embedding optimization is dominated
    by many small tensor operations where CUDA launch overhead is slower.
    """
    if device is None or str(device).lower() == "auto":
        return "cuda" if torch.cuda.is_available() else "cpu"
    return device


def resolve_edge_weight(w, mode="signed"):
    """Map data correlations to semantic edge weights."""
    mode = str(mode).lower()
    if mode in ("signed", "raw"):
        return float(w)
    if mode in ("abs", "magnitude"):
        return abs(float(w))
    if mode in ("positive", "pos", "clip"):
        return max(float(w), 0.0)
    raise ValueError(f"unknown edge_weight_mode: {mode}")


def relative_loading_polarity(g, edge_weights, left, right, eps=1e-9):
    """Return the loading-sign relation for two observed nodes.

    The relation is invariant to flipping the global sign of a latent score: two
    loadings flip together, so the sign of their product is unchanged.
    """
    if edge_weights is None:
        return "unavailable"
    shared = set(g.parents(left)) & set(g.parents(right))
    signs = []
    for parent in shared:
        product = float(edge_weights.get((parent, left), 0.0)) * float(
            edge_weights.get((parent, right), 0.0)
        )
        if abs(product) > eps:
            signs.append(1 if product > 0 else -1)
    if not signs:
        return "unavailable"
    if all(sign > 0 for sign in signs):
        return "same"
    if all(sign < 0 for sign in signs):
        return "opposite"
    return "ambiguous"


def observed_label_priors(g, corr, obs, visible, labeled_emb, scope="siblings",
                          polarity_mode="corr_positive", edge_weights=None,
                          return_details=False):
    """Build item-level priors for masked observed nodes from visible observed labels.

    The causal graph gates which labels may inform a masked observed variable; correlations only set
    the local mixture weights. This keeps factor-level graph structure while preserving item-level
    distinctions within a factor.
    """
    scope = str(scope).lower()
    polarity_mode = str(polarity_mode).lower()
    if polarity_mode not in ("corr_positive", "loading_same"):
        raise ValueError(f"unknown observed prior polarity mode: {polarity_mode}")
    if polarity_mode == "loading_same" and edge_weights is None:
        raise ValueError("loading_same prior requires edge_weights")
    visible = list(visible)
    visible_set = set(visible)
    priors, details = {}, {}
    for i, name in enumerate(obs):
        if i in visible_set:
            continue
        scope_candidates = []
        for j in visible:
            if scope == "siblings":
                if not (set(g.parents(name)) & set(g.parents(obs[j]))):
                    continue
            elif scope != "all":
                raise ValueError(f"unknown observed prior scope: {scope}")
            scope_candidates.append(j)
        relations = {
            j: relative_loading_polarity(g, edge_weights, name, obs[j])
            for j in scope_candidates
        }
        candidates = [
            j for j in scope_candidates
            if polarity_mode != "loading_same" or relations[j] == "same"
        ]
        info = {
            "polarity_mode": polarity_mode,
            "scope_candidates": [obs[j] for j in scope_candidates],
            "eligible_candidates": [obs[j] for j in candidates],
            "prior_available": False,
            "contributors": [],
        }
        if not candidates:
            details[name] = info
            continue
        candidate_corr = np.asarray(corr[i, candidates], dtype=float)
        weights = (np.clip(candidate_corr, 0.0, None)
                   if polarity_mode == "corr_positive" else np.abs(candidate_corr))
        if weights.sum() < 1e-9:
            details[name] = info
            continue
        v = sum(float(w) * labeled_emb[obs[j]] for w, j in zip(weights, candidates)) / weights.sum()
        priors[name] = v / (np.linalg.norm(v) + 1e-9)
        info["prior_available"] = True
        info["contributors"] = [
            {
                "variable": obs[j],
                "correlation": float(correlation),
                "weight": float(weight),
                "normalized_weight": float(weight / weights.sum()),
                "loading_relation": relations[j],
            }
            for j, correlation, weight in zip(candidates, candidate_corr, weights)
            if weight > 0
        ]
        details[name] = info
    return (priors, details) if return_details else priors


def optimize_embeddings(g, W, labeled_emb, d, steps=1500, lr=5e-2, lam_zero=0.3, lam_norm=0.1,
                        seed=0, device="cpu", verbose=False, edge_weight_mode="signed",
                        normalize_gen=False, observed_prior_emb=None, lam_obs_prior=0.0):
    """g: graph.Graph; W: dict edge->signed weight (given support, data-estimated);
    labeled_emb: dict observed_name -> np.array[d] (frozen, VISIBLE labels only).
    Returns dict node_name -> np.array[d] for ALL nodes (labeled ones pass through unchanged)."""
    import torch
    torch.manual_seed(seed)
    device = resolve_device(torch, device)
    labeled = set(labeled_emb)
    free = [n for n in g.nodes if n not in labeled]                  # latents + unlabeled observed
    if not free:
        return dict(labeled_emb)
    # init: each free node starts at the weighted mean of its labeled neighbours (parents/children), else 0
    init = {}
    for n in free:
        nb = [p for p in g.parents(n) if p in labeled] + [c for c in g.children(n) if c in labeled]
        if nb:
            v = np.mean([labeled_emb[x] for x in nb], 0)
            init[n] = v / (np.linalg.norm(v) + 1e-9)
        else:
            init[n] = np.zeros(d)
    E = {n: torch.nn.Parameter(torch.tensor(init[n], dtype=torch.float32, device=device)) for n in free}
    A = {n: torch.tensor(v, dtype=torch.float32, device=device) for n, v in labeled_emb.items()}
    P = {}
    if observed_prior_emb:
        P = {n: torch.tensor(v, dtype=torch.float32, device=device)
             for n, v in observed_prior_emb.items() if n in E}

    def emb(n):
        return A[n] if n in A else E[n]

    def gen(n):
        ps = g.parents(n)
        if not ps:
            return None
        tot = None
        denom = 0.0
        for p in ps:
            w = resolve_edge_weight(W.get((p, n), 0.0), edge_weight_mode)
            t = w * emb(p)
            tot = t if tot is None else tot + t
            denom += abs(w)
        if normalize_gen and denom > 1e-9:
            tot = tot / denom
        return tot

    zero_pairs = [(a, b) for a, b in g.independent_pairs()]
    opt = torch.optim.Adam(list(E.values()), lr=lr)
    for step in range(steps):
        opt.zero_grad()
        loss = 0.0
        for n in g.nodes:
            gn = gen(n)
            if gn is None:
                continue
            if n in A:                                               # visible label: graph must generate it
                loss = loss + ((gn - A[n]) ** 2).sum()
            else:                                                    # free node: equals its generation
                loss = loss + ((E[n] - gn) ** 2).sum()
        if zero_pairs and lam_zero > 0:
            left = torch.stack([emb(a) for a, _ in zero_pairs])
            right = torch.stack([emb(b) for _, b in zero_pairs])
            left = left / (left.norm(dim=1, keepdim=True) + 1e-9)
            right = right / (right.norm(dim=1, keepdim=True) + 1e-9)
            loss = loss + lam_zero * ((left * right).sum(dim=1) ** 2).mean()
        if P and lam_obs_prior > 0:
            op = 0.0
            for n, pv in P.items():
                op = op + ((E[n] - pv) ** 2).sum()
            loss = loss + lam_obs_prior * op / len(P)
        if lam_norm > 0:
            nr = 0.0
            for n in free:
                nr = nr + (E[n].norm() - 1.0) ** 2
            loss = loss + lam_norm * nr / len(free)
        loss.backward()
        opt.step()
        if verbose and (step % 300 == 0 or step == steps - 1):
            print(f"  [optimize step {step}/{steps} loss={float(loss):.4f}]", flush=True)
    out = {n: v.detach().cpu().numpy().astype(np.float64) for n, v in E.items()}
    out.update({n: np.asarray(v, np.float64) for n, v in labeled_emb.items()})
    return out
